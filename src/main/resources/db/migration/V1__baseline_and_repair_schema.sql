SET start_time = COALESCE(day.start_time, schedule.daily_start_time),
    end_time = COALESCE(day.end_time, schedule.daily_end_time),
    start_place_name = COALESCE(day.start_place_name, schedule.start_place_name),
    start_longitude = COALESCE(day.start_longitude, schedule.start_longitude),
    start_latitude = COALESCE(day.start_latitude, schedule.start_latitude),
