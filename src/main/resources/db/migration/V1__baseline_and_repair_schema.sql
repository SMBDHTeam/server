CREATE TABLE IF NOT EXISTS questions (
    id varchar(255) PRIMARY KEY,
    text varchar(255) NOT NULL,
    type varchar(255) NOT NULL,
    required boolean NOT NULL,
    display_order integer NOT NULL,
    active boolean NOT NULL
);

CREATE TABLE IF NOT EXISTS answers (
    id varchar(255) PRIMARY KEY,
    label varchar(255) NOT NULL,
    display_order integer NOT NULL,
    active boolean NOT NULL,
    question_id varchar(255) NOT NULL REFERENCES questions(id)
);

CREATE TABLE IF NOT EXISTS places (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    source varchar(255) NOT NULL,
    external_content_id varchar(255) NOT NULL,
    content_type_id varchar(255),
    name varchar(255) NOT NULL,
    category varchar(255),
    address varchar(255),
    longitude numeric(12, 8) NOT NULL,
    latitude numeric(12, 8) NOT NULL,
    primary_image_url text,
    created_at timestamp NOT NULL,
    updated_at timestamp NOT NULL,
    CONSTRAINT uk_places_source_external_content_id UNIQUE (source, external_content_id)
);

CREATE TABLE IF NOT EXISTS place_details (
    place_id bigint PRIMARY KEY REFERENCES places(id),
    overview text,
    homepage text,
    raw_json text
);

CREATE TABLE IF NOT EXISTS place_operating_infos (
    place_id bigint PRIMARY KEY REFERENCES places(id),
    opening_hours_text text,
    closed_days_text text,
    use_fee_text text,
    parking_text text,
    requires_manual_check boolean NOT NULL,
    raw_json text
);

CREATE TABLE IF NOT EXISTS place_images (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    place_id bigint NOT NULL REFERENCES places(id),
    url text NOT NULL,
    thumbnail_url text,
    copyright_type varchar(255),
    display_order integer NOT NULL
);

CREATE TABLE IF NOT EXISTS schedules (
    id uuid PRIMARY KEY,
    status varchar(255) NOT NULL,
    start_date date NOT NULL,
    end_date date NOT NULL,
    daily_start_time time NOT NULL,
    daily_end_time time NOT NULL,
    start_place_name varchar(255) NOT NULL,
    start_longitude numeric(12, 8) NOT NULL,
    start_latitude numeric(12, 8) NOT NULL,
    end_place_name varchar(255) NOT NULL,
    end_longitude numeric(12, 8) NOT NULL,
    end_latitude numeric(12, 8) NOT NULL,
    style_summary text,
    condition_json text NOT NULL,
    created_at timestamp NOT NULL,
    updated_at timestamp NOT NULL
);

CREATE TABLE IF NOT EXISTS schedule_days (
    id uuid PRIMARY KEY,
    schedule_id uuid NOT NULL REFERENCES schedules(id),
    day_no integer NOT NULL,
    date date NOT NULL,
    start_time time NOT NULL,
    end_time time NOT NULL,
    start_place_name varchar(255) NOT NULL,
    start_longitude numeric(12, 8) NOT NULL,
    start_latitude numeric(12, 8) NOT NULL,
    end_place_name varchar(255) NOT NULL,
    end_longitude numeric(12, 8) NOT NULL,
    end_latitude numeric(12, 8) NOT NULL,
    CONSTRAINT uk_schedule_days_schedule_day_no UNIQUE (schedule_id, day_no)
);

CREATE TABLE IF NOT EXISTS schedule_stops (
    id uuid PRIMARY KEY,
    schedule_day_id uuid NOT NULL REFERENCES schedule_days(id),
    place_id bigint NOT NULL REFERENCES places(id),
    stop_order integer NOT NULL,
    stay_minutes integer NOT NULL,
    selection_reasons_json text NOT NULL,
    warnings_json text NOT NULL,
    CONSTRAINT uk_schedule_stops_day_order UNIQUE (schedule_day_id, stop_order)
);

CREATE TABLE IF NOT EXISTS transit_routes (
    id uuid PRIMARY KEY,
    schedule_day_id uuid NOT NULL REFERENCES schedule_days(id),
    schedule_stop_id uuid UNIQUE REFERENCES schedule_stops(id),
    route_type varchar(255) NOT NULL,
    route_order integer NOT NULL,
    total_minutes integer NOT NULL,
    fare_amount integer,
    provider varchar(255) NOT NULL,
    realtime_status varchar(255) NOT NULL,
    fallback_used boolean NOT NULL,
    warnings_json text NOT NULL,
    raw_json text,
    CONSTRAINT uk_transit_routes_day_order UNIQUE (schedule_day_id, route_order)
);

CREATE TABLE IF NOT EXISTS transit_segments (
    id uuid PRIMARY KEY,
    transit_route_id uuid NOT NULL REFERENCES transit_routes(id),
    segment_order integer NOT NULL,
    mode varchar(255) NOT NULL,
    line_name varchar(255),
    start_station_id varchar(255),
    start_station_name varchar(255),
    end_station_id varchar(255),
    end_station_name varchar(255),
    instruction text NOT NULL,
    duration_minutes integer NOT NULL,
    distance_meters integer,
    station_count integer,
    wait_minutes integer NOT NULL,
    realtime_status varchar(255) NOT NULL,
    CONSTRAINT uk_transit_segments_route_order UNIQUE (transit_route_id, segment_order)
);

CREATE TABLE IF NOT EXISTS transit_route_lines (
    id uuid PRIMARY KEY,
    transit_route_id uuid NOT NULL REFERENCES transit_routes(id),
    line_order integer NOT NULL,
    mode varchar(255) NOT NULL,
    line_name varchar(255),
    coordinates_json text NOT NULL,
    duration_minutes integer,
    distance_meters integer,
    instruction text,
    fallback_used boolean NOT NULL,
    CONSTRAINT uk_transit_route_lines_route_order UNIQUE (transit_route_id, line_order)
);

CREATE TABLE IF NOT EXISTS share_links (
    id uuid PRIMARY KEY,
    schedule_id uuid NOT NULL REFERENCES schedules(id),
    token_hash varchar(64) NOT NULL,
    expires_at timestamp,
    revoked_at timestamp,
    created_at timestamp NOT NULL,
    CONSTRAINT uk_share_links_token_hash UNIQUE (token_hash)
);

